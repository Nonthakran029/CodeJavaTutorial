public class WhileLoop {
    public static void main(String[] args) {
        for(int i = 1; i <= 10; i++){
            System.out.println("reslut in forloop : "+i);
        }
        int iWhie = 1;
        while (iWhie <= 10) {
            System.out.println("reslut in forloop : "+iWhie);
            iWhie += 2;
        }
    }
}
